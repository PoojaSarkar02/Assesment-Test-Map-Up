import pandas as pd
import numpy as np

def find_ids_within_ten_percentage_threshold(unrolled_df, reference_id):
    filtered_df = unrolled_df[unrolled_df['id_start'] == reference_id]

    if filtered_df.empty:
        return []

    average_distance = filtered_df['distance'].mean()

    lower_bound = average_distance * 0.90
    upper_bound = average_distance * 1.10

    ids_within_threshold = unrolled_df[
        (unrolled_df['distance'] >= lower_bound) &
        (unrolled_df['distance'] <= upper_bound)
    ]['id_start'].unique()

    sorted_ids = sorted(ids_within_threshold)

    return sorted_ids


