import pandas as pd
import datetime

def calculate_time_based_toll_rates(unrolled_df):
    weekday_discounts = {
        'morning': 0.8,  # From 00:00 to 10:00
        'daytime': 1.2,  # From 10:00 to 18:00
        'evening': 0.8   # From 18:00 to 23:59
    }
    weekend_discount = 0.7

    # Define the days of the week
    days_of_week = ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday']

    # Create time ranges for weekdays
    morning_start = datetime.time(0, 0, 0)
    morning_end = datetime.time(10, 0, 0)
    daytime_start = datetime.time(10, 0, 0)
    daytime_end = datetime.time(18, 0, 0)
    evening_start = datetime.time(18, 0, 0)
    evening_end = datetime.time(23, 59, 59)

    unrolled_df['start_day'] = days_of_week[0]
    unrolled_df['end_day'] = days_of_week[-1]
    unrolled_df['start_time'] = morning_start
    unrolled_df['end_time'] = evening_end

    for vehicle_type in ['moto', 'car', 'rv', 'bus', 'truck']:
        adjusted_rates = []

        for _, row in unrolled_df.iterrows():
            start_day = row['start_day']
            start_time = row['start_time']
            distance = row[vehicle_type]

            # Determine the discount factor
            if start_day in ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday']:
                # Weekday
                if morning_start <= start_time < morning_end:
                    discount = weekday_discounts['morning']
                elif daytime_start <= start_time < daytime_end:
                    discount = weekday_discounts['daytime']
                else:
                    discount = weekday_discounts['evening']
            else:
                # Weekend
                discount = weekend_discount

            adjusted_rate = distance * discount
            adjusted_rates.append(adjusted_rate)

        unrolled_df[vehicle_type] = adjusted_rates

    return unrolled_df


