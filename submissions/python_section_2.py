import pandas as pd

def calculate_distance_matrix(file_path):
    df = pd.read_csv(file_path)

    unique_ids = pd.concat([df['id_start'], df['id_end']]).unique()

    distance_matrix = pd.DataFrame(index=unique_ids, columns=unique_ids)

    for _, row in df.iterrows():
        from_id = row['id_start']
        to_id = row['id_end']
        distance = row['distance']

        distance_matrix.at[from_id, to_id] = distance
        distance_matrix.at[to_id, from_id] = distance

    for id in unique_ids:
        distance_matrix.at[id, id] = 0

    for i in range(len(unique_ids)):
        for j in range(len(unique_ids)):
            if pd.isna(distance_matrix.iat[i, j]):
                for k in range(len(unique_ids)):
                    if not pd.isna(distance_matrix.iat[i, k]) and not pd.isna(distance_matrix.iat[k, j]):
                        distance_matrix.iat[i, j] = distance_matrix.iat[i, k] + distance_matrix.iat[k, j]
                        distance_matrix.iat[j, i] = distance_matrix.iat[i, j]
                        break

    return distance_matrix

file_path = 'https://raw.githubusercontent.com/mapup/MapUp-DA-Assessment-2024/refs/heads/main/datasets/dataset-2.csv'
