import pandas as pd

def unroll_distance_matrix(distance_matrix):
    id_start = []
    id_end = []
    distances = []

    for i in range(len(distance_matrix)):
        for j in range(len(distance_matrix)):
            start_id = distance_matrix.index[i]
            end_id = distance_matrix.columns[j]
            distance = distance_matrix.iat[i, j]

            if start_id != end_id and not pd.isna(distance):
                id_start.append(start_id)
                id_end.append(end_id)
                distances.append(distance)

    unrolled_df = pd.DataFrame({
        'id_start': id_start,
        'id_end': id_end,
        'distance': distances
    })

    return unrolled_df

